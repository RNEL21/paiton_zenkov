# функция для вывода приветствия
# принимает один параметр - имя
def print_hello(name):
  print(f"Hello, {name}!")

# функция для нахождения наибольшего общего делителя (нод)
def gcd(a, b):
  # цикл работает, пока b не станет 0
  while b != 0:
    # в 'a' записываем 'b', а в 'b' — остаток от деления 'a' на 'b'
    a, b = b, a % b
  # когда цикл завершается, в 'a' находится нод
  return a

# основная часть программы
print("введите номер задания (1 или 2):")
task_number = input()

if task_number == "1":
  print("введите ваше имя:")
  user_name = input()
  # вызываем функцию для первого задания
  print_hello(user_name)
elif task_number == "2":
  print("введите первое натуральное число:")
  number1 = int(input())
  print("введите второе натуральное число:")
  number2 = int(input())
  # вызываем функцию для второго задания
  result = gcd(number1, number2)
  print(f"наибольший общий делитель: {result}")
else:
  print("неверный номер задания.")